export default {    
    convertNumberToLetter(number) {
        let n = parseInt(number);
        if(n == 1) return 'one'
        else if(n == 2) return 'two'
        else if(n == 3) return 'three'
        else if(n == 4) return 'four'
        else if(n == 5) return 'five'
        else if(n == 6) return 'six'
    }
}